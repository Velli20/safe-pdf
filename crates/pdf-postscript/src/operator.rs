#[derive(Debug, PartialEq, Clone)]
pub enum Operator {
    Add,
    Sub,
    Mul,
    Div,
    Dup,
    Exch,
    Pop,
    Eq,
    Ne,
    Gt,
    Lt,
    Ge,
    Le,
    And,
    Or,
    Not,
    Copy,
    Roll,
    Sqrt,
    Abs,
    Cvi,
    Mod,
    Truncate,
    If(Vec<Operator>),
    IfElse(Vec<Operator>, Vec<Operator>),
    Number(f64),
}
